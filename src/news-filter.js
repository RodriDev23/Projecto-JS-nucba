document.addEventListener("DOMContentLoaded", function () {
    const searchForm = document.getElementById("search-form");
    const searchInput = document.getElementById("search-input");
    const filteredNewsContainer = document.getElementById('filtered-news');

    searchForm.addEventListener("submit", function (event) {
        event.preventDefault();

        const searchTerm = searchInput.value.toLowerCase();
        console.log(searchTerm);
        filterNewsByCategory(searchTerm);
    });

    function filterNewsByCategory(searchTerm) {
        // Limpiamos el contenido antiguo
        filteredNewsContainer.innerHTML = '';

        for (const newsKey in newsFilter) {
            const newsItem = newsFilter[newsKey];
            if (newsItem.category.toLowerCase().includes(searchTerm) || searchTerm === newsFilter.category) {
                createNewsElement(newsItem);
            }
        }

        if (filteredNewsContainer.children.length === 0) {
            filteredNewsContainer.innerHTML = "No se encontraron noticias.";
        }
        console.log(`Categoría filtrada: ${searchTerm}`);
    }

   function createNewsElement(newsItem) {
    const itemContainer = document.createElement('div');
    itemContainer.classList.add('flex', 'flex-col', 'mb-4', 'px-4', 'lg:w-1/4', 'md:w-1/2', 'w-full', "justify-center", "items-center");



    const titleElement = document.createElement('h3');
    titleElement.textContent = newsItem.title;

    const imgElement = document.createElement('img');
    imgElement.classList.add('w-[50%]',"rounded-md");
    imgElement.src = newsItem.img;

    itemContainer.appendChild(titleElement);
    itemContainer.appendChild(imgElement);

    filteredNewsContainer.appendChild(itemContainer);
}

filteredNewsContainer.classList.add('grid', 'grid-cols-1', 'sm:grid-cols-2', 'md:grid-cols-3', 'lg:grid-cols-4', 'gap-4', 'mt-4');

});


